package AVC3.GestaoDeCondominios;

public class Funcionario {
    public class Funcionário {
        private String nome;
        private String cargo;
    
        public Funcionário(String nome, String cargo) {
            this.nome = nome;
            this.cargo = cargo;
        }
    
        public String getNome() {
            return nome;
        }
    
        public void setNome(String nome) {
            this.nome = nome;
        }
    }
    
}
